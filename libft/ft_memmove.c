#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	if (src < dst)
	{
		while (len > 0)
		{
			len--;
			*(char *)(dst + len) = *(const char *)(src + len);
		}
	}
	if (src > dst)
		return (ft_memcpy(dst, src, len));
	return (dst);
}
